void main() {
  // Arthmetic operators
  // + - * / %
  int a = 10;
  int b = 20;

  print("Addition: ${a + b}");
  print("Subtraction: ${a - b}");
  print("Mulitiplication: ${a * b}");
  print("Division: ${a / b}");
  print("Modulus: ${a % b}");

  // Assignment operators
  // = += -= *= /= %=
  int c = 10;
  c += 5;
  print("c += 5 -> $c");
  c -= 5;
  print("c -= 5 -> $c");
  c *= 5;
  print("c *= 5 -> $c");
  c ~/= 5; 
print("c /= 5 -> $c");

}
